package factoryRequest;

public class CRequestInfo {

    private String Url;
    private String Body;


    public CRequestInfo() {}

    public String getUrl() {
        return Url;
    }

    public void setUrl(String url) {
        Url = url;
    }

    public String getBody() {
        return Body;
    }

    public void setBody(String body) {
        Body = body;
    }
}
